export const getUptime = () => {
  if (!uptime || !uptime.months || uptime.months.length === 0) {
    return [] // Return an empty array if uptime data is not available or empty
  }

  // Use flatMap to flatten the array of days across all months
  return uptime.months.flatMap(month => month.days)
}

export const getUptimePercentage = () => {
  if (!uptime || !uptime.months || uptime.months.length === 0) {
    return 0 // Return 0 if uptime data is not available or empty
  }

  const totalMonths = uptime.months.length
  const totalUptimePercentage = uptime.months.reduce((acc, m) => acc + m.uptime_percentage, 0)

  const averageUptimePercentage = (totalUptimePercentage / totalMonths) * 100

  return averageUptimePercentage.toFixed(2) // Display 2 decimal places
}

